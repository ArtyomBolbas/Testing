package model;

public class Answer {

	private String answer;

	private FullAnswer fullAnswer;

	public String getAnswer() {
		return answer;
	}

	public void setAnswer(String answer) {
		this.answer = answer;
	}

	public FullAnswer getFullAnswer() {
		return fullAnswer;
	}

	public void setFullAnswer(FullAnswer fullAnswer) {
		this.fullAnswer = fullAnswer;
	}

}
